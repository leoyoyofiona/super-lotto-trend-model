function parseBalls(value: string) {
  return value
    .split(' ')
    .map((item) => Number(item))
    .sort((a, b) => a - b)
}

function parseMoney(value: string) {
  return Number(value.replace(/,/g, ''))
}

function getWeek(date: string) {
  return ['日', '一', '二', '三', '四', '五', '六'][new Date(`${date}T00:00:00`).getDay()]
}
